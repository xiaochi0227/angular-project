  // 每日销售对账导出
  addexport() {
    const params = this.fetchSearchParams();
    let reqParams = {
      'path': '/test-export-bsp/saledetail/getsaledetailtime',
      'paths': { 'time': '/test-export-bsp/saledetail/getsaledetailtime', 'month': '/test-report-bsp/salesdetail/bspreport/getsaledetailmonth' },
      'search': params,
      'desc': '运营统计-类别周销售明细',
      'export_columns': [{ 'name': 'encode' }]
    };
    this.http.addexport.request(reqParams)
      .subscribe(row => {
        if (row) {
          this.notification.create('success', '温馨提示', row['msg']);
        } else {
          this.notification.create('error', '温馨提示', '添加失败,请重试！');
        }
      });
  }

  // 初始化searchModel
  initSearchModel() {
    let newDate = new Date();
    const year = newDate.getFullYear();
    let month: any = newDate.getMonth() + 1;
    month = (month < 10 ? '0' + month : month);
    this.searchModel.dateRange = [new Date(Date.now() - 3600 * 24 * 6 * 1000), new Date(Date.now())];
    this.searchModel.month = month;
  }

