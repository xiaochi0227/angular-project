  // 查询
  search() {
    if (this.loading) {
      return;
    }
    // 判断月份
    const dateRange = this.searchModel.dateRange;
    const month = this.searchModel.month;
    const startDateStr = this.datePipe.transform(dateRange[0], 'yyyy-MM-dd').substring(5, 7);
    const endDeteStr = this.datePipe.transform(dateRange[1], 'yyyy-MM-dd').substring(5, 7);
    if (month !== startDateStr && month !== endDeteStr) {
      this.notification.create('error', '温馨提示', '请选择所选月份内的时间段查询！');
    } else {
      this.getData();
    }
  }

  // 封装查询参数
  fetchSearchParams() {
    const dateRange = this.searchModel.dateRange;
    const month = this.searchModel.month;
    const startDateStr = this.datePipe.transform(dateRange[0], 'yyyy-MM-dd 00:00:00');
    const endDeteStr = this.datePipe.transform(dateRange[1], 'yyyy-MM-dd 23:59:59');
    this.showStartDate = startDateStr.substring(0, 10);
    this.showEndDate = endDeteStr.substring(0, 10);
    this.showMonth = startDateStr.substring(0, 7);
    const params = {
      date: startDateStr.substr(0, 8) + '01',
      startDate: startDateStr,
      endDate: endDeteStr
    };
    return params;
  }

  findItem(list, name) {
    const filterList = list.filter(item => item['name'] === name);
    return filterList[0];
  }

  // 封装数据用于显示表格
  transformData() {
    this.categoryList.forEach(cate => {
      let itemObj = {};
      this.detailColSpan = 0;
      this.monthColSpan = 0;
      let xWidth = 300;
      this.saledetails.forEach(detail => {
        this.detailColSpan += detail.channels.length;
        detail.channels.forEach(channel => {
          const item = this.findItem(channel.onlineCategories, cate.name);
          if (item) {
            itemObj[detail.city + channel.code + 'detailMoney'] = item.money;
          }
          xWidth += 100;
          this.widthConfig.push('100px');
        });
      });
      this.saledetailmonth.forEach(detail => {
        this.monthColSpan += detail.channels.length;
        detail.channels.forEach(channel => {
          const item = this.findItem(channel.onlineCategories, cate.name);
          if (item) {
            itemObj[detail.city + channel.code + 'monthMoney'] = item.money;
          }
          xWidth += 100;
          this.widthConfig.push('100px');
        });
      });
      this.list = [...this.list, Object.assign({
        name: cate.name,
        supName: cate.supName
      }, itemObj)];
      this.scrollConfig = {
        x: xWidth + 'px',
        y: '600px'
      };
    });
  }

